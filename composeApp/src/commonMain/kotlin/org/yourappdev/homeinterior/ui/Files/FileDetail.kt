@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun CreateEditScreen(imageUrl: String,
                     onClick: () -> Unit) {
    val scope = rememberCoroutineScope()
    val backgroundColor = Color(0xFFFFFFFF)
    val darkText = Color(0xFF2C2C2C)
    val grayText = Color(0xFF9B9B9B)
    val buttonBackground = Color(0xFFF9F9F9)
    val borderColor = Color(0xFFA3B18A)
    val imageBorder = Color(0xFFEAEAEA)

    val bottomSheetState = rememberModalBottomSheetState(skipPartiallyExpanded = true)
    var showDelete by remember {
        mutableStateOf(false)
    }
    val gradientColors = listOf(
        Color(0xFFC5EBB2),
        Color(0xFFDFF2C2),
        Color(0xFFC1DFB5),
        Color(0xFFD2F7BD)
    )

    Box(
        modifier = Modifier
            .fillMaxSize()
            .background(backgroundColor)
    ) {
        Column(
            modifier = Modifier
                .fillMaxSize()
                .statusBarsPadding()
        ) {
            TopBar(
                darkText = darkText,
                gradientColors = gradientColors
            ) {
                onClick()
            }

            Spacer(modifier = Modifier.height(24.dp))

            ImageSection(
                imageUrl = imageUrl,
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(horizontal = 24.dp),
                imageBorder = imageBorder
            )

            Spacer(modifier = Modifier.height(24.dp))


            Row(
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(horizontal = 24.dp),
                horizontalArrangement = Arrangement.spacedBy(10.dp),
                verticalAlignment = Alignment.CenterVertically
            ) {
                ActionButton(
                    icon = Res.drawable.redo,
                    label = "Redo",
                    backgroundColor = buttonBackground,
                    textColor = darkText,
                    iconTint = grayText,
                    modifier = Modifier.weight(1f)
                ) {

                }

                ActionButton(
                    icon = Res.drawable.save,
                    label = "Save",
                    backgroundColor = buttonBackground,
                    textColor = darkText,
                    iconTint = grayText,
                    modifier = Modifier.weight(1f)
                ) {

                }

                ActionButton(
                    icon = Res.drawable.deletenew,
                    label = "Delete",
                    backgroundColor = buttonBackground,
                    textColor = darkText,
                    iconTint = grayText,
                    modifier = Modifier.weight(1f)
                ) {
                    showDelete = true
                }

                ActionButton(
                    icon = Res.drawable.share,
                    label = "Share",
                    backgroundColor = Color.White,
                    textColor = darkText,
                    iconTint = grayText,
                    hasBorder = true,
                    borderColor = borderColor,
                    modifier = Modifier.weight(1f)
                ) {}

            }
        }
        if (showDelete) {
            ModalBottomSheet(
                onDismissRequest = { showDelete = false },
                sheetState = bottomSheetState,
                containerColor = Color.Transparent,
                dragHandle = null, modifier = Modifier.statusBarsPadding()
            ) {
                DeleteConfirmationDialog(title = "Are you sure you want to delete files?") {
                    scope.launch {
                        bottomSheetState.hide()
                        showDelete = false
                    }
                }
            }
        }
    }
}

@Composable
fun TopBar(
    darkText: Color,
    gradientColors: List<Color>,
    onClick: () -> Unit
) {
    Row(
        modifier = Modifier
            .fillMaxWidth()
            .padding(horizontal = 24.dp, vertical = 8.dp),
        verticalAlignment = Alignment.CenterVertically,
        horizontalArrangement = Arrangement.SpaceBetween
    ) {
        Row(
            verticalAlignment = Alignment.CenterVertically,
            horizontalArrangement = Arrangement.Start,
            modifier = Modifier.weight(1f)
        ) {
            CloseIconButton(iconSize = 20.dp) {
                onClick()
            }
            Spacer(modifier = Modifier.width(8.dp))

            Text(
                text = "Create",
                fontSize = 20.sp,
                fontWeight = FontWeight.SemiBold,
                color = darkText
            )
        }

        ProBadge(gradientColors)
    }
}

@Composable
fun ProBadge(gradientColors: List<Color>) {
    Box(
        modifier = Modifier
            .height(32.dp)
            .clip(RoundedCornerShape(21.dp))
            .background(
                brush = Brush.linearGradient(
                    colors = gradientColors
                )
            )
            .padding(horizontal = 17.dp),
        contentAlignment = Alignment.Center
    ) {
        Text(
            text = "PRO",
            fontSize = 15.sp,
            fontWeight = FontWeight.ExtraBold,
            color = Color.Black
        )
    }
}

@Composable
fun ImageSection(
    imageUrl: String,
    modifier: Modifier = Modifier,
    imageBorder: Color
) {
    var image by remember { mutableStateOf(Res.drawable.sofa_3) }
    var isPressed by remember { mutableStateOf(false) }
    Box(
        modifier = modifier
            .aspectRatio(392f / 620f)
            .clip(RoundedCornerShape(9.dp))
            .border(1.dp, imageBorder, RoundedCornerShape(9.dp))
            .background(Color(0xFFF5F5F5))
    ) {
        AsyncImage(
            model = if (isPressed) imageUrl else imageUrl, // Yahan aap secondary image logic laga sakte hain
            contentDescription = "Editing Image",
            contentScale = ContentScale.Crop,
            modifier = Modifier.fillMaxSize()
        )

        Box(
            modifier = Modifier
                .size(42.dp)
                .align(Alignment.BottomStart)
                .offset(x = 16.dp, y = (-16).dp)
                .clip(RoundedCornerShape(41.dp))
                .background(Color(0xFFFCFCFC).copy(alpha = 0.9f))
                .padding(7.dp)
                .pointerInput(Unit) {
                    detectTapGestures(
                        onPress = {
                            isPressed = true
                            tryAwaitRelease()
                            isPressed = false
                        }
                    )
                },
            contentAlignment = Alignment.Center
        ) {
            Icon(
                painter = painterResource(Res.drawable.compare),
                contentDescription = "Layers",
                tint = Color(0xFFADAAAA),
                modifier = Modifier.size(24.dp)
            )
        }
    }
}


@Composable
fun ActionButton(
    icon: DrawableResource,
    label: String,
    backgroundColor: Color,
    textColor: Color,
    iconTint: Color,
    modifier: Modifier = Modifier,
    hasBorder: Boolean = false,
    borderColor: Color = Color.Transparent,
    onClick: () -> Unit
) {
    Box(
        modifier = modifier
            .height(72.dp)
            .shadow(
                elevation = if (hasBorder) 6.dp else 0.dp,
                shape = RoundedCornerShape(8.dp),
                ambientColor = Color.Black.copy(alpha = 0.23f),
                spotColor = Color.Black.copy(alpha = 0.23f)
            )
            .clip(RoundedCornerShape(8.dp))
            .background(backgroundColor)
            .then(
                if (hasBorder) {
                    Modifier.border(1.dp, borderColor, RoundedCornerShape(8.dp))
                } else {
                    Modifier
                }
            ).clickable(enabled = true, onClick = {
                onClick()
            })
    ) {
        Column(
            modifier = Modifier.fillMaxSize().padding(horizontal = 10.dp, vertical = 7.dp),
            horizontalAlignment = Alignment.CenterHorizontally,
            verticalArrangement = Arrangement.Center
        ) {
            Image(
                painter = painterResource(icon),
                contentDescription = label,
                colorFilter = ColorFilter.tint(color = iconTint),
                modifier = Modifier.size(24.dp)
            )

            Spacer(modifier = Modifier.height(6.dp))

            Text(
                text = label,
                fontSize = 13.sp,
                fontWeight = FontWeight.Medium,
                color = textColor
            )
        }
    }

}

